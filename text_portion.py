@torch.no_grad()
def get_text_embs(pipe=None, prompt=None, in_embs=None):
    if prompt == None:
        prompt = 'the scene'
    
    if in_embs != None:
        in_embs = in_embs / in_embs.abs().max() * 1.
        
    # run just prompt to get input activations
    out = pipe.text_encoder(pipe.tokenizer(prompt, return_tensors='pt').input_ids.to('cuda'), 
                            output_hidden_states=True,
                            input_hidden_states=in_embs.to('cuda') if in_embs != None else 0)
    activations = out.hidden_states[PLUCK_LAYER][:, 1:-1].mean(1)
    for_unet = out.last_hidden_state
    
    
    return for_unet, activations.detach().to(torch.float32).to('cpu')




